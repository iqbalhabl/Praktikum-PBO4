    private void urutan(){
        try{
            Connection x=koneksi.getHubung();
            Statement y=x.createStatement();
            
            String sql="SELECT MAX(id) FROM users";
            ResultSet z=y.executeQuery(sql);
            
            if(z.next()){
                int a=z.getInt(1);
                idid.setText(Integer.toString(a+1));
            }
        z.close();
        y.close();
        }catch(Exception e){
            System.out.println("Gagal Daftar");
        }
    }

