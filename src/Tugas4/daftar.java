    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String id = idid.getText().trim();
        String username = user.getText().trim();
        String password = pass.getText().trim();
        
        try{
            Connection x=koneksi.getHubung();
            Statement y=x.createStatement();

            PreparedStatement sql=x.prepareStatement("SELECT * FROM users WHERE username=?");
            sql.setString(1, username);
            
            ResultSet z=sql.executeQuery();
            if(z.next()){
                JOptionPane.showMessageDialog(null, "-Username terpakai-");
            }
            else{
                if(password.equals("") || username.equals("")){
                    JOptionPane.showMessageDialog(null, "Isikan Password dan Username!");
                }
                else{
                    String b="INSERT INTO users VALUES (?, ?, ?)";
                    PreparedStatement c=x.prepareStatement(b);
                    c.setString(1, id);
                    c.setString(2, username);
                    c.setString(3, password);
                    c.executeUpdate();
                    c.close();
                    JOptionPane.showMessageDialog(null, "Telah terdaftar");
                    this.dispose();
                    login d=new login();
                    d.setVisible(true);
                }
            }
        }catch(SQLException e){
            System.out.println("Gagal Login");
        }
    }//GEN-LAST:event_simpanActionPerformed

